# 5) Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. 
# Você não pode ver as lâmpadas da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. 
# Seu objetivo é descobrir qual interruptor controla qual lâmpada. 
# Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor controla cada lâmpada?  

# Primeira ida: Primeiramente ligaria o primeiro interruptor e deixaria alguns minutos ligado, depois de alguns minutos
# desligaria e ligaria o segundo interruptor e faria minha primeira ida à alguma das salas, chegando lá, se a lampada
# estiver acesa, ela é controlada pelo segundo interruptor, se estiver apagada e quente, é controlada pelo primeiro
# se estiver apagada e fria, é controlada pelo terceiro.

# Segunda ida: Na minha segunda ida, com o segundo interruptor ainda ligado, verificaria se a lampada está acesa, 
# se sim, é controlada pelo segundo interruptor, se estiver apagada ela é controlada pelo terceiro interruptor, já que a lampada que é 
# controlada pelo primeiro interruptor está na primeira sala.